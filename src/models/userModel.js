import mongoose, { Schema } from "mongoose";
import jwt from "jsonwebtoken";
import bcrypt from "bcrypt";

const userSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      lowecase: true,
      trim: true,
      index: true, //helps to find in search faster:indepth alot xa
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    fullName: {
      type: String,
      required: true,
      trim: true,
      index: true,
    },
    avatar: {
      type: String,
      required: true,
    },
    coverImage: {
      type: String,
    },
    watchHistory: {
      type: Schema.Types.ObjectId,
      ref: "video",
    },
    password: {
      type: String,
      required: [true, "password is required"],
    },
    refreshToken: {
      type: String,
    },
  },
  { timestamps: true }
);

// pre is a mongoose plugin, userSchema ma pre chaluxau tiki save huna aghi kam garnu xa, pasword encript garne
// fat arrow function nagarne because it doesn't know context: userlai save garne event hudai xa
// so we use normal fnc, ani yesma pani async lauxau coz there is lot inner processes
// coz it's middleware need to pass next

userSchema.pre("save", async function (next) {
  if (!this.isModified("password")) return next();
  this.password = bcrypt.hash(this.password, 10);
  next();
});

// bcrypt vitra ni process dherai hunxa, bcrypt le ta password encript garera save garko hunxa, feri pani access garna lai encriped password ta user le send gardaina,
// adding another method. like middleware mongoose allow us to inject method. updateOne deleteOne jastai isPassword chai hamile afaiile banayeko costom method ho, we make it to compair passowrd [plus bcrypt le ni compair fnc dinxa]

userSchema.methods.isPasswordCorrect = async function (password) {
  return await bcrypt.compare(password, this.password);
};

userSchema.methods.generateAccessToken = function () {
  jwt.sign(
    {
      _id: this._id,
      email: this.email,
      username: this.username,
    },
    process.env.ACCESS_TOKEN_SECERT,
    {
      expiresIn: process.env.ACCESS_TOKEN_EXPIRY,
    }
  );
};

userSchema.methods.generateRefreshToken = function () {
  jwt.sign(
    {
      _id: this._id,
    },
    process.env.REFRESH_TOKEN_SECRET,
    {
      expiresIn: process.env.REFRESH_TOKEN_EXPIRY,
    }
  );
};

export const User = model("User", userSchema);
